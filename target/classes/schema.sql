 CREATE TABLE amortization (
        id INT NOT NULL AUTO_INCREMENT, 
        MYUSER VARCHAR(30) NOT NULL,
        EMAIL VARCHAR(30),
        DATE VARCHAR(30),   
        MONTHLYINTEREST VARCHAR(30),
        MONTHLYPRINCIPAL VARCHAR(30),
        TOTALINTEREST VARCHAR(30),
        TOTALPRINCIPAL VARCHAR(30),
        PRIMARY KEY (ID)
 );